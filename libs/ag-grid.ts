/**
 * 첫 번째 행에서 ArrowUp 키를 막는 suppressKeyboardEvent
 */
export const suppressArrowUpOnFirstRow = (params: SuppressKeyboardEventParams): boolean => {
  const { event, node } = params;
  const keyboardEvent = event as KeyboardEvent;

  return keyboardEvent.key === 'ArrowUp' && node.rowIndex === 0;
};

export const withCommonKeyboardSuppress = (colDef: ColDef): ColDef => {
  return {
    ...colDef,
    suppressKeyboardEvent: suppressArrowUpOnFirstRow,
  };
};

export const suppressDeleteKey = (params: SuppressKeyboardEventParams): boolean => {
  const keyboardEvent = params.event as KeyboardEvent;
  return keyboardEvent.key === 'Delete';
};

